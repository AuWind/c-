/*头文件：使用库函数*/
#include<cstdio>				//标准输入、输出：scanf printf
#include<iostream>				//流输入、输出：cin cout
using namespace std;			//声明默认命名空间：std::cin std::cout
int main(){						//程序入口函数：程序运行的入口
    printf("Hello world\n");	//使用标准输出字符和换行
    cout << "Hello world" << endl;	//使用流输出字符和换行
    return 0;					//程序正常退出返回0，竞赛要求必须
}


/*
1、最简程序由三部分组成：头文件、命名空间、入口函数。
2、部分的作用以及语法。
3、程序设计就是对主函数的内容设计
*/