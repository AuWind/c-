/*
9.判断能否被3，5，7整除【1.4编程基础之逻辑表达式与条件分支09】
    给定一个整数，判断它能否被3，5，7整除，并输出以下信息：
    1、能同时被3，5，7整除（直接输出3 5 7，每个数中间一个空格）；
    2、只能被其中两个数整除（输出两个数，小的在前，大的在后。例如：3 5或者 3 7或者5 7，中间用空格分隔）；
    3、只能被其中一个数整除（输出这个除数）；
    4、不能被任何数整除，输出小写字符‘n’，不包括单引号。
输入:
    输入一行，包括一个整数。
输出:
    输出一行，按照描述要求给出整数被3，5，7整除的情况。
样例输入:
    105
样例输出:
    3 5 7
*/
#include<iostream>
#include<cstdio>
using namespace std;
int main(){
	int n;
	cin >> n;
	if(n%3 == 0 || n%5 == 0 || n%7 ==0){
		if(n%3 == 0 && n%5 == 0 && n%7 ==0) cout << "3 5 7\n";
		else{
			if((n%3 == 0 && n%5 == 0) || (n%3 == 0 && n%7 == 0) || (n%5 == 0 && n%7 == 0)){
				if(n%3 == 0 && n%5 == 0)cout << "3 5\n";
				if(n%3 == 0 && n%7 == 0)cout << "3 7\n";
				if(n%5 == 0 && n%7 == 0)cout << "5 7\n";
			}else{
				if(n%3 == 0)cout << "3\n";
				if(n%5 == 0)cout << "5\n";
				if(n%7 == 0)cout << "7\n";
			}
		}
	}else cout << "n\n";
	return 0;
}
